package com.review.class02;

public class StringConcatenationDemo {
	
	public static void main(String[] args) {
		
		// I love Castro
		
		String name = "Castro";
		
		System.out.println("I love " + name);
		
		String statement = "I love Castro very much";
		
		System.out.println(statement);
		
		System.out.println("========================================================================");
	}

}
